package com.yj.bj.entity;

public class Number {
	
	private Long id;
	
	private String level;
	
	private String num;
	
	private String amount;
	
	private String first;
	
	private String second;
	
	private String brushFirst;
	
	private String brushSecond;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getFirst() {
		return first;
	}

	public void setFirst(String first) {
		this.first = first;
	}

	public String getSecond() {
		return second;
	}

	public void setSecond(String second) {
		this.second = second;
	}

	public String getBrushFirst() {
		return brushFirst;
	}

	public void setBrushFirst(String brushFirst) {
		this.brushFirst = brushFirst;
	}

	public String getBrushSecond() {
		return brushSecond;
	}

	public void setBrushSecond(String brushSecond) {
		this.brushSecond = brushSecond;
	}
}
